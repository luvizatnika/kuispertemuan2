public class Elearning {
    String nim;
    String nama;

    public String getNim() {
        return nim;
    }

    public void setNim(String nim) {
        this.nim = nim;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public Elearning(String nim, String nama) {
        this.nim = nim;
        this.nama = nama;
    }
}

